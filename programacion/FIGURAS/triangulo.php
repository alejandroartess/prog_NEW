<?php
class triangulo {
    private $base=5.2;
    private $altura=3;
function_construct(){

    public function getBase()
    {
        return $this->base;
    }
 
    public function setBase($base)
    {
        $this->base = $base;

        return $this;
    }

    public function getAltura()
    {
        return $this->altura;
    }

    public function setAltura($altura)
    {
        $this->altura = $altura;

        return $this;
    }
}

?>